package TrickyJavaQuestions;

public class PrintOneToHundred {

	public static void main(String[] args) {

		/**
		 * Output numbers from 1...100 
		 * Without using any numbers in your code
		 */

		int one = 'C' / 'C';
		String s = "**********";

		for (int i = one; i <= (s.length() * s.length()); i++) {
			System.out.println(i);
		}

		System.out.println("*****");

		// ASCII value of a -97 , b -98 , c - 99 , d-100
		for (int i = one; i <= 100; i += one) {
			System.out.println(i);
		}

	}

}
